package project.model;

import java.io.Serializable;

import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;

@Entity
public class Level_2_Level_3 implements Serializable{
	
	private static final long serialVersionUID = 1L;
			
	@Id
	@ManyToOne
	@JoinColumn(name="id_level_2")
	private Level_2 id_level_2;
	
	@ManyToOne
	@JoinColumn(name="id_level_3")
	private Level_3 id_level_3;

	public Level_2 getId_level_2() {
		return id_level_2;
	}

	public void setId_level_2(Level_2 id_level_2) {
		this.id_level_2 = id_level_2;
	}

	public Level_3 getId_level_3() {
		return id_level_3;
	}

	public void setId_level_3(Level_3 id_level_3) {
		this.id_level_3 = id_level_3;
	}
	
}
